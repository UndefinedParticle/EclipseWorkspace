package myCode;

import java.util.Scanner;

public class Pattern1 {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter number of line => ");
		int n=sc.nextInt();
		for(int i=1;i<=n;i++) {
			for(int j=1;j<=n-i+1;j++) {
				System.out.print(" ");
			}
			for(int k=1;k<=i;k++) {
				System.out.print("* ");
			}
			System.out.println();
		}
		System.out.println();
		for(int i=1;i<=n;i++) {
			
			for(int k=1;k<=i;k++) {
				System.out.print(" ");
			}
			
			for(int j=1;j<=n-i+1;j++) {
				System.out.print("* ");
			}
			
			System.out.println();
		}


	}

}
